import math

class Vector:
    def _init_(self, coordenada_x, coordenada_y):
        self.x = coordenada_x
        self.y = coordenada_y
        self.magnitud = math.sqrt(self.x * 2 + self.y * 2)
        self.angulo = math.atan2(self.y, self.x)

    def _str_(self):
        respuesta = "( " + str(self.x) + " , " + str(self.y) + " )"
        return respuesta

    def _sub_(self, otra):
        respuesta = Vector(self.x - otra.x, self.y - otra.y)
        return respuesta

    def _add_(self, otra):
        respuesta = Vector(self.x + otra.x, self.y + otra.y)
        return respuesta

    def escalar(self, otra):
        return self.x * otra.x + self.y * otra.y

    def producto_punto(self, otro):
        resultado = self.x * otro.x + self.y * otro.y
        return resultado

    def producto_cruz(self, otro):
        respuesta = self.x * otro.y - self.y * otro.x
        return respuesta

